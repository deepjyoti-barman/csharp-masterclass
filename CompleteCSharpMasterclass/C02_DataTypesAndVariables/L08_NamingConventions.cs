/*
NOTE:
- Class name should use pascal casing i.e. a capital letter at the beginning and a capital letter on every word that comes afterwards (e.g. ClientActivity)
- Method name should also use pascal casing (e.g. CalculateValue)
- Method arguments should use camel casing (e.g. firstNumber)
- Local variables should use camel casing (e.g. itemCount)
- Try to avoid abbreviations (e.g. use 'userControl' instead of using 'usrCtrl', exception XML, URI etc.)
- Don't use numbers at the start of variables, arguments, method or class names
- Try to avoid underscore (e.g. login_date, exception _loginDate)
- Use noun phrases to name classes
- Use primitive data types over reference types (e.g. use string whenever possible instead of String)
- Try to avoid capital letters while naming constants
- C# Coding Standards in detail: https://www.dofactory.com/csharp-coding-standards
*/
